package com.practice.functionalinterface;

class Person {
	int id;
	String fname;
	String lname;
	String phone;
	/**
	 * @param id
	 * @param fname
	 * @param lname
	 * @param phone
	 */
	public Person(int id, String fname, String lname, String phone) {
		this.id = id;
		this.fname = fname;
		this.lname = lname;
		this.phone = phone;
	}
}